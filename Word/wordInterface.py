from abc import abstractmethod

class WordInterface:

    @abstractmethod
    def to_capitalize_without_accent():
        pass

    @abstractmethod
    def to_lowercase_without_accent():
        pass

    @abstractmethod
    def to_uppercase_without_accent():
        pass

    @abstractmethod
    def remove_accent_from_words():
        pass

    @abstractmethod
    def words_to_uppercase():
        pass

    @abstractmethod
    def words_to_lowercase():
        pass

    @abstractmethod
    def capitalize_words():
        pass

    @abstractmethod
    def uppercase_words_without_accents():
        pass

    @abstractmethod
    def lowercase_words_without_accents():
        pass

    @abstractmethod
    def concat_methods_results():
        pass







